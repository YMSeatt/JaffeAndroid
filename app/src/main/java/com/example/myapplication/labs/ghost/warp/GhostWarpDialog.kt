package com.example.myapplication.labs.ghost.warp

import android.content.Intent
import android.graphics.RuntimeShader
import android.os.Build
import androidx.compose.animation.core.*
import androidx.compose.foundation.Canvas
import androidx.compose.foundation.layout.*
import androidx.compose.foundation.rememberScrollState
import androidx.compose.foundation.verticalScroll
import androidx.compose.material3.*
import androidx.compose.runtime.*
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.graphics.ShaderBrush
import androidx.compose.ui.platform.LocalContext
import androidx.compose.ui.text.font.FontFamily
import androidx.compose.ui.text.font.FontWeight
import androidx.compose.ui.unit.dp
import androidx.compose.ui.unit.sp

/**
 * GhostWarpDialog: A glassmorphic interface for classroom curvature (spacetime) reports.
 *
 * Displays the Markdown-formatted analysis generated by the [GhostWarpEngine]
 * within an AGSL-backed dialog.
 */
@Composable
fun GhostWarpDialog(
    report: String,
    onDismiss: () -> Unit
) {
    val context = LocalContext.current

    AlertDialog(
        onDismissRequest = onDismiss,
        title = {
            Text(
                text = "GHOST WARP: SPACETIME ANALYSIS",
                style = MaterialTheme.typography.titleLarge.copy(
                    color = Color.Cyan,
                    fontWeight = FontWeight.Bold,
                    letterSpacing = 1.2.sp
                )
            )
        },
        text = {
            Column(
                modifier = Modifier
                    .fillMaxWidth()
                    .heightIn(max = 450.dp)
                    .verticalScroll(rememberScrollState()),
                horizontalAlignment = Alignment.CenterHorizontally
            ) {
                // Curvature Shader Background Box
                Box(
                    modifier = Modifier
                        .fillMaxWidth()
                        .height(140.dp)
                        .padding(vertical = 8.dp)
                ) {
                    if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.TIRAMISU) {
                        val infiniteTransition = rememberInfiniteTransition(label = "warp_dialog_pulse")
                        val time by infiniteTransition.animateFloat(
                            initialValue = 0f,
                            targetValue = 6.28f,
                            animationSpec = infiniteRepeatable(
                                animation = tween(8000, easing = LinearEasing),
                                repeatMode = RepeatMode.Restart
                            ),
                            label = "time"
                        )

                        val shader = remember { RuntimeShader(GhostWarpShader.NEURAL_WARP) }
                        val brush = remember(shader) { ShaderBrush(shader) }

                        Canvas(modifier = Modifier.fillMaxSize()) {
                            shader.setFloatUniform("iResolution", size.width, size.height)
                            shader.setFloatUniform("iTime", time)
                            shader.setFloatUniform("iIntensity", 1.0f)
                            shader.setIntUniform("iPointCount", 0) // Decorative background
                            drawRect(brush = brush)
                        }
                    } else {
                        Surface(
                            modifier = Modifier.fillMaxSize(),
                            color = Color.DarkGray.copy(alpha = 0.3f),
                            shape = MaterialTheme.shapes.medium
                        ) {
                            Box(contentAlignment = Alignment.Center) {
                                Text("Warp Visualizer (API 33+)", color = Color.Gray)
                            }
                        }
                    }
                }

                // Report Content
                Surface(
                    color = Color.Black.copy(alpha = 0.4f),
                    shape = MaterialTheme.shapes.small,
                    modifier = Modifier.fillMaxWidth().padding(top = 8.dp)
                ) {
                    Text(
                        text = report,
                        style = MaterialTheme.typography.bodySmall.copy(
                            fontFamily = FontFamily.Monospace,
                            lineHeight = 16.sp,
                            color = Color.LightGray
                        ),
                        modifier = Modifier.padding(14.dp)
                    )
                }
            }
        },
        confirmButton = {
            TextButton(
                onClick = {
                    val intent = Intent(Intent.ACTION_SEND).apply {
                        type = "text/plain"
                        putExtra(Intent.EXTRA_SUBJECT, "Ghost Warp: Classroom Curvature Analysis")
                        putExtra(Intent.EXTRA_TEXT, report)
                    }
                    context.startActivity(Intent.createChooser(intent, "Share Warp Analysis"))
                }
            ) {
                Text("EXPORT ANALYSIS", color = Color.Cyan)
            }
        },
        dismissButton = {
            TextButton(onClick = onDismiss) {
                Text("DISMISS")
            }
        },
        containerColor = Color(0xFF00050A),
        textContentColor = Color.LightGray
    )
}
